# ── FIX-1 + FIX-3: extract_features with explicit temporal column names ───────
def extract_features(rows, fset="engineered"):
    """
    Returns X (ndarray), y (ndarray), feat_cols (list of str).

    FIX-1: temporal columns are explicitly named and appended to feat_cols so
           X.shape[1] == len(feat_cols) at all times.
    FIX-3: feat_cols is complete before SelectKBest is called in main(), so
           get_support(indices=True) correctly maps back to named columns.
    """
    if fset == "voltage_only":
        static_cols = [f"V_N{n}" for n in range(1, 12)]
    else:
        all_candidate = (
            [f"V_N{n}" for n in range(1, 12)] +
            [f"DEV_N{n}_pct" for n in range(1, 12)] +
            [f"I_N{n}_mA" for n in [2, 3, 5, 6, 8, 9, 11]] +
            [
                "V_min", "V_max", "V_range", "V_mean", "V_std",
                "dev_max_abs", "nodes_above_threshold",
                "N5_N6_diff", "N2_N3_diff", "N8_N9_diff",
                "worst_node_isolation", "temperature_C",
                "routing_signature", "branch_symmetry_error",
                "isolation_index", "signal_uniformity",
                "max_branch_delta", "cross_coupling_estimate",
            ]
        )
        sample_keys = set(rows[0].keys())
        static_cols = [c for c in all_candidate
                       if c not in LEAKAGE_COLS and c in sample_keys]

    # FIX-1: build explicit temporal column names (4 stats × 11 nodes)
    has_temporal = f"V_N1_t0" in rows[0]
    temporal_cols = []
    if has_temporal:
        for n in range(1, 12):
            temporal_cols.extend([
                f"V_N{n}_tmean",
                f"V_N{n}_tstd",
                f"V_N{n}_tdelta",
                f"V_N{n}_trange",
            ])

    feat_cols = static_cols + temporal_cols  # complete, named list

    X = []
    for r in rows:
        row_feats = [float(r.get(c, 0.0)) for c in static_cols]
        if has_temporal:
            for n in range(1, 12):
                try:
                    seq = np.array([float(r[f"V_N{n}_t{t}"])
                                    for t in range(N_TEMPORAL_STEPS)])
                    row_feats.extend([
                        float(seq.mean()),
                        float(seq.std()),
                        float(seq[-1] - seq[0]),
                        float(seq.max() - seq.min()),
                    ])
                except (KeyError, ValueError):
                    row_feats.extend([0.0, 0.0, 0.0, 0.0])
        X.append(row_feats)

    X = np.array(X, dtype=np.float64)
    y = np.array([r["fault_label"] for r in rows])

    assert X.shape[1] == len(feat_cols), (
        f"[BUG] X.shape[1]={X.shape[1]} != len(feat_cols)={len(feat_cols)}"
    )
    return X, y, feat_cols, static_cols, temporal_cols


def extract_location_labels(rows):
    def to_branch(loc):
        nodes_in_loc = [int(n) for n in loc.split("_") if n.isdigit()]
        for n in nodes_in_loc:
            if n in [1, 2, 3]:  return "Branch_A"
            if n in [4, 5, 6]:  return "Branch_B"
            if n in [7, 8, 9]:  return "Branch_C"
            if n in [10, 11]:   return "Branch_D"
        return "None"
    return np.array([to_branch(r.get("fault_location", "None")) for r in rows])


# ── FIX-C: PHYSICS SEVERITY (mirrors generator formula) ──────────────────────
def compute_physics_severity(devs, branch_sym, iso_idx, routing_sig):
    da  = [abs(d) for d in devs]
    wvd = min(1.0, max(da) / 100.0)
    bi  = min(1.0, branch_sym / 1.0)
    ie  = min(1.0, abs(1.0 - iso_idx))
    ra  = min(1.0, routing_sig / 2.0)
    return round(min(100.0, (0.45 * wvd + 0.25 * bi + 0.15 * ie + 0.15 * ra) * 100.0), 2)


# ── BUILD MODELS ──────────────────────────────────────────────────────────────
def build_models():
    rf = RandomForestClassifier(
        n_estimators=300, max_depth=None,
        min_samples_split=2, random_state=42, n_jobs=-1)

    dt = DecisionTreeClassifier(
        max_depth=15, min_samples_split=4, random_state=42)

    knn = KNeighborsClassifier(n_neighbors=5, n_jobs=-1)

    svm_pipeline = Pipeline([
        ("scaler", StandardScaler()),
        ("svm", CalibratedClassifierCV(
            SVC(kernel="rbf", C=10, gamma="scale",
                probability=True, random_state=42),
            cv=3, method="isotonic")),
    ])

    models = {
        "Random Forest": rf,
        "Decision Tree": dt,
        "k-NN (k=5)":    knn,
        "SVM (RBF)":     svm_pipeline,
    }

    if XGB_OK:
        xgb_model = xgb.XGBClassifier(
            n_estimators=300, max_depth=6, learning_rate=0.1,
            subsample=0.8, colsample_bytree=0.8,
            eval_metric="mlogloss", random_state=42, n_jobs=-1)
        models["XGBoost"] = xgb_model

        base_ensemble = VotingClassifier(
            estimators=[
                ("rf", RandomForestClassifier(
                    n_estimators=200, random_state=42, n_jobs=-1)),
                ("xgb", xgb.XGBClassifier(
                    n_estimators=200, max_depth=5, learning_rate=0.1,
                    eval_metric="mlogloss", random_state=42, n_jobs=-1)),
                ("svm", Pipeline([
                    ("scaler", StandardScaler()),
                    ("svm", CalibratedClassifierCV(
                        SVC(kernel="rbf", C=10, gamma="scale",
                            probability=True, random_state=42),
                        cv=3, method="isotonic")),
                ])),
            ],
            voting="soft")

        # FIX-4: calibration off by default — too expensive unless explicitly enabled
        if ENABLE_ENSEMBLE_CALIBRATION:
            ensemble = CalibratedClassifierCV(base_ensemble, cv=3, method="isotonic")
        else:
            ensemble = base_ensemble

        models[FINAL_PREDICT_MODEL] = ensemble

    return models


# ── TRAIN & EVALUATE ──────────────────────────────────────────────────────────
def train_evaluate(X_train, X_test, y_train, y_test, feat_cols, le):
    scaler = StandardScaler()
    scaler.fit(X_train)

    y_tr_enc = le.transform(y_train)
    y_te_enc = le.transform(y_test)

    models = build_models()
    results = {}

    print("\n" + "=" * 60)
    print("MODEL TRAINING & EVALUATION  [v6.1 — all fixes applied]")
    print("=" * 60)

    skf = StratifiedKFold(n_splits=5, shuffle=True, random_state=42)

    for name, model in models.items():
        print(f"\n[{name}] Training...")
        is_xgb = name == "XGBoost"
        ytr = y_tr_enc if is_xgb else y_train
        yte = y_te_enc if is_xgb else y_test

        model.fit(X_train, ytr)

        y_pred_raw = model.predict(X_test)
        if is_xgb:
            y_pred = le.inverse_transform(y_pred_raw)
        else:
            y_pred = y_pred_raw

        y_prob = model.predict_proba(X_test)

        # FIX-7: reorder probability columns to match FAULT_LABELS order
        if is_xgb:
            xgb_classes = [le.inverse_transform([c])[0] for c in model.classes_]
            col_order = [xgb_classes.index(fl)
                         for fl in FAULT_LABELS if fl in xgb_classes]
            y_prob = y_prob[:, col_order]
        else:
            model_classes = list(model.classes_)
            col_order = []
            for fl in FAULT_LABELS:
                if fl in model_classes:
                    col_order.append(model_classes.index(fl))
            if len(col_order) == len(FAULT_LABELS):
                y_prob = y_prob[:, col_order]

        y_pred_thresh = np.array([
            p if y_prob[i].max() >= CONFIDENCE_THRESHOLD else "UNCERTAIN"
            for i, p in enumerate(y_pred)
        ])

        acc   = accuracy_score(y_test, y_pred)
        mask  = y_pred_thresh != "UNCERTAIN"
        acc_t = accuracy_score(y_test[mask], y_pred_thresh[mask]) if mask.any() else acc
        n_unc = int((~mask).sum())

        cv    = cross_val_score(model, X_train, ytr,
                                cv=skf, scoring="accuracy", n_jobs=-1)
        cv_m, cv_s = cv.mean(), cv.std()

        mcc = matthews_corrcoef(y_test, y_pred)
        try:
            y_bin = label_binarize(y_test, classes=FAULT_LABELS)
            auc   = roc_auc_score(y_bin, y_prob, multi_class="ovr", average="macro")
        except Exception:
            auc = float("nan")

        class_conf_acc = {}
        for fl in FAULT_LABELS:
            mask_c = (np.array(y_test) == fl) & mask
            class_conf_acc[fl] = (accuracy_score(np.array(y_test)[mask_c],
                                                  y_pred_thresh[mask_c])
                                  if mask_c.any() else float("nan"))

        cm = confusion_matrix(y_test, y_pred, labels=FAULT_LABELS)
        cr = classification_report(y_test, y_pred, labels=FAULT_LABELS)

        # FIX-5: persistent fault metric REMOVED from evaluation
        # (shuffled test set has no temporal order — metric is meaningless)
        # FaultConfirmer is only used in dashboard/inference.

        print(f"  Raw Accuracy     : {acc * 100:.2f}%")
        print(f"  Confident Acc    : {acc_t * 100:.2f}%  "
              f"(UNCERTAIN: {n_unc}/{len(y_pred)} = {n_unc/len(y_pred)*100:.1f}%)")
        print(f"  CV Accuracy      : {cv_m * 100:.2f}% +/- {cv_s * 100:.2f}%")
        print(f"  MCC              : {mcc:.4f}")
        print(f"  ROC-AUC (OvR)    : {auc:.4f}")

        results[name] = dict(
            model=model, acc=acc, acc_thresh=acc_t,
            cv_mean=cv_m, cv_std=cv_s, cm=cm, cr=cr,
            y_pred=y_pred, y_pred_thresh=y_pred_thresh,
            y_prob=y_prob, n_uncertain=n_unc,
            scaled=False,
            mcc=mcc, auc=auc, class_conf_acc=class_conf_acc,
            is_xgb=is_xgb,
        )

    return results, scaler


