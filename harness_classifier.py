# ── FIX-E: PERSISTENT FAULT CONFIRMATION ─────────────────────────────────────
class FaultConfirmer:
    """
    Debounce logic: confirm a fault only after N consecutive identical readings.
    Used in dashboard / real-time inference only (not in shuffled evaluation).
    """
    def __init__(self, n_required=3):
        self.n_required = n_required
        self._last  = None
        self._count = 0

    def update(self, label):
        if label == self._last:
            self._count += 1
        else:
            self._last  = label
            self._count = 1
        if self._count >= self.n_required:
            return label
        return None

    def reset(self):
        self._last = None; self._count = 0


