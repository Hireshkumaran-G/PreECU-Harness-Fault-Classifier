# ── CALIBRATION DIAGNOSTICS ──────────────────────────────────────────────────
def run_calibration_diagnostics(results, y_test):
    print("\n  [Calibration] Computing diagnostics...")
    report_lines = [
        "=" * 70,
        "PROBABILITY CALIBRATION REPORT  v6.1",
        "AI-Driven Pre-ECU Harness Integrity & Routing Validation",
        f"Generated: {datetime.now().isoformat()}",
        "=" * 70,
        "",
        "Brier Score: lower = better (0 = perfect). ~0.33 = random for 6 classes.",
        "ECE: Expected Calibration Error (mean |confidence - accuracy| over bins).",
        "",
        f"NOTE: SHAP explanations use '{FINAL_EXPLAIN_MODEL}' as surrogate for the",
        f"      deployed '{FINAL_PREDICT_MODEL}' predictor.",
        f"      Ensemble calibration: {'ON' if ENABLE_ENSEMBLE_CALIBRATION else 'OFF (default)'}.",
        "",
    ]

    fig_rows = len(results)
    if PLOT:
        fig, axes = plt.subplots(fig_rows, 1,
                                 figsize=(10, 4 * fig_rows), facecolor="white")
        if fig_rows == 1:
            axes = [axes]

    for ax_i, (name, res) in enumerate(results.items()):
        y_prob = res["y_prob"]
        y_pred = res["y_pred"]

        y_bin = label_binarize(y_test, classes=FAULT_LABELS)
        brier = np.mean([
            brier_score_loss(y_bin[:, i], y_prob[:, i])
            for i in range(len(FAULT_LABELS))
        ])

        max_conf = y_prob.max(axis=1)
        correct  = (y_pred == np.array(y_test)).astype(float)
        bins     = np.linspace(0, 1, 11)
        ece      = 0.0
        n_total  = len(y_test)
        bin_accs = []
        for lo, hi in zip(bins[:-1], bins[1:]):
            mask = (max_conf >= lo) & (max_conf < hi)
            n_b  = mask.sum()
            if n_b > 0:
                acc_b  = correct[mask].mean()
                conf_b = max_conf[mask].mean()
                ece   += n_b / n_total * abs(acc_b - conf_b)
                bin_accs.append(acc_b)
            else:
                bin_accs.append(0)

        report_lines += [
            f"--- {name} ---",
            f"  Brier Score : {brier:.4f}",
            f"  ECE         : {ece:.4f}",
            "",
        ]

        if PLOT:
            ax = axes[ax_i]
            bin_centers = [(bins[i] + bins[i+1]) / 2 for i in range(10)]
            ax.bar(bin_centers, bin_accs, width=0.09,
                   color="#2471A3", alpha=0.8, label="Accuracy")
            ax.plot([0, 1], [0, 1], "k--", lw=1, label="Perfect calibration")
            ax.set_xlabel("Confidence"); ax.set_ylabel("Accuracy")
            ax.set_title(f"Reliability Diagram — {name}  "
                         f"(Brier={brier:.3f}, ECE={ece:.3f})",
                         fontsize=10, fontweight="bold")
            ax.legend(fontsize=8); ax.grid(alpha=0.3)
            ax.set_xlim(0, 1); ax.set_ylim(0, 1)

    if PLOT:
        plt.tight_layout()
        p = os.path.join(OUT, "calibration_reliability.png")
        plt.savefig(p, dpi=150, bbox_inches="tight")
        plt.close()
        print(f"  Plot: {p}")

    cal_path = os.path.join(OUT, "calibration_report.txt")
    with open(cal_path, "w") as f:
        f.write("\n".join(report_lines))
    print(f"  Saved: {cal_path}")


# ── LEAKAGE AUDIT ─────────────────────────────────────────────────────────────
def audit_feature_leakage(X, y, feat_cols, le):
    print("\n  [Leakage Audit] Computing mutual information...")
    y_enc = le.transform(y)
    mi    = mutual_info_classif(X, y_enc, random_state=42)
    idx   = np.argsort(mi)[::-1][:15]

    audit_path = os.path.join(OUT, "leakage_audit.txt")
    with open(audit_path, "w") as f:
        f.write("FEATURE LEAKAGE AUDIT — v6.1\n")
        f.write("=" * 60 + "\n")
        f.write("worst_node_idx REMOVED from training (FIX-A).\n")
        f.write("severity / fault_detected excluded (analysis-only cols).\n")
        f.write("signal_uniformity = mean(per-branch variance) (FIX-B).\n")
        f.write("Temporal features scaled separately before selection (FIX-8).\n\n")
        f.write(f"{'Rank':<6} {'Feature':<34} {'MI Score':<12} {'Risk'}\n")
        f.write("-" * 60 + "\n")
        HIGH_MI = ["worst_node_isolation", "routing_signature", "cross_coupling_estimate"]
        for rank, i in enumerate(idx, 1):
            fname = feat_cols[i]
            risk  = "REVIEW" if fname in HIGH_MI else "OK"
            f.write(f"{rank:<6} {fname:<34} {mi[i]:<12.4f} {risk}\n")
    print(f"  Saved: {audit_path}")
    return mi


